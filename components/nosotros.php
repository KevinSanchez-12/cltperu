<h1 id="nosotros" class="titulo">Nosotros</h1>
<section class="nosotros container">
    <p>Somos una entidad autónoma de derecho público interno, representativo de los Profesionales en turismo de la
        República con sede en la ciudad de Lima de conformidad con el artículo 33 de la Constitución política del Perú.
    </p>
    <br>
    <p>Nuestra <b>misión</b> es representar y agrupar a los Licenciados en Turismo profesionales del Perú, de todas las
        especialidades, que cautela y preserva el comportamiento ético de sus miembros y debe asegurar al Perú que
        cuenta con una profesión nacional que ejerce el área de Turismo en un contexto de orden, respeto,
        competitividad, calidad y ética, y que está enraizada en sus valores sociales, culturales y políticos, como base
        fundamental en el proceso de desarrollo de la nación.</p>
    <br>
    <p>Nuestra <b>visión</b> es ser reconocida como una institución sólida, que patrocina el manejo eficiente del
        conocimiento, con la finalidad de orientar a la sociedad peruana en las grandes decisiones, fomentando la
        practica de valores y comportamiento ético de los Licenciados en Turismo profesionales, así como elevando la
        calidad del área Turismo, apoyando el crecimiento del país en el contexto de la globalización.</p>
</section>